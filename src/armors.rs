use rab_core::armor_and_skills::{Gender, Skill};

pub struct ArmorDesc {
    pub skills: &'static [(Skill, u8)],
    pub slots: &'static [u8],
    pub rare: u8,
    pub defense: u8,
    pub fire: i8,
    pub water: i8,
    pub thunder: i8,
    pub ice: i8,
    pub dragon: i8,
    pub gender: Option<Gender>,
}

impl From<&Armor> for rab_core::armor_and_skills::Armor {
    fn from(armor: &Armor) -> Self {
        let ArmorDesc {
            skills,
            slots,
            rare,
            defense,
            fire,
            water,
            thunder,
            ice,
            dragon,
            gender,
        } = armor.description();
        rab_core::armor_and_skills::Armor {
            skills: skills.to_vec(),
            slots: slots.to_vec(),
            rare,
            defense,
            fire,
            water,
            thunder,
            ice,
            dragon,
            gender,
            name: armor.to_string(),
        }
    }
}

